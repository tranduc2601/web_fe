//tri tuyet doi
console.log(Math.abs(-5.5)); // 5.5 - Math.abs() trả về giá trị tuyệt đối của số, bỏ dấu âm.
console.log(Math.abs(10));   // 10 - Giá trị tuyệt đối của 10 là chính nó.
//lam tron so
console.log(Math.ceil(4.4)); // 5 - Math.ceil() làm tròn lên đến số nguyên gần nhất.
console.log(Math.ceil(5.8)); // 6 - Làm tròn lên đến số nguyên gần nhất.
// lay phan nguyen cua so thuc
console.log(Math.floor(5.7)); // 5 - Math.floor() làm tròn xuống đến số nguyên gần nhất.
console.log(Math.floor(6.1)); // 6 - Làm tròn xuống đến số nguyên gần nhất.
// lam tron so theo toan ngoaif chojw
console.log(Math.round(10.1)); // 10 - Math.round() làm tròn đến số nguyên gần nhất, 10.1 làm tròn xuống.
console.log(Math.round(20.6)); // 21 - 20.6 làm tròn lên vì phần thập phân lớn hơn hoặc bằng 0.5.
